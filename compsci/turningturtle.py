# this program is the turning turtle with inputs

import turtle
thomas = turtle.Turtle()
first = int(input("Enter a number: "))
second = int(input("Enter a number: "))
third = int(input("Enter a number: "))
fourth = int(input("Enter a number: "))
fifth = int(input("Enter a number: "))

for i in range(1):
    thomas.forward(100)
    thomas.left(first)
    thomas.forward(100)
    thomas.left(second)
    thomas.forward(100)
    thomas.left(third)
    thomas.forward(100)
    thomas.left(fourth)
    thomas.forward(100)
    thomas.left(fifth)
    thomas.forward(100)

